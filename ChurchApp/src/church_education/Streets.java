    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int r = jTable1.getSelectedRow();
        r = (int) jTable1.getValueAt(r, 1);
        try{
            int f = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete the street with ID = " + Integer.toString(r), "Alert", JOptionPane.YES_NO_OPTION);
            if(f == 0){
                pst = (PreparedStatement) con.prepareStatement("delete from Streets where StreetID = ?");
                pst.setInt(1, r);
                pst.executeUpdate();
            }
        }catch(Exception e){
            int f = JOptionPane.showConfirmDialog(null, "هذا الشارع موجود كعنوان لشخص او اكثر هل انت متاكد من حذفه !", "Alert", JOptionPane.YES_NO_OPTION);
            if(f == 0){
                try {
                    pst = (PreparedStatement) con.prepareStatement("update Persons set StreetID = null where StreetID = ?");
                    pst.setInt(1, r);
                    pst.executeUpdate();
                    pst = (PreparedStatement) con.prepareStatement("update Persons set StreetID2 = null where StreetID2 = ?");
                    pst.setInt(1, r);
                    pst.executeUpdate();
                    pst = (PreparedStatement) con.prepareStatement("delete from Streets where StreetID = ?");
                    pst.setInt(1, r);
                    pst.executeUpdate();
                } catch (SQLException ex) {
                    Logger.getLogger(Served.class.getName()).log(Level.SEVERE, null, ex);
                }
            }        
        }
        SearchActionPerformed(evt);
    }//GEN-LAST:event_jButton2ActionPerformed

