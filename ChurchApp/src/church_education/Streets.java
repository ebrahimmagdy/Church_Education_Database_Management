    public void insertAll(){
        try {
            st = (Statement) con.createStatement();
            rs = st.executeQuery("SELECT * FROM Streets");
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.getDataVector().removeAllElements();
            while(rs.next()){
                raw = new Object[5];
                raw[0] = false;
                raw[1] = rs.getInt("StreetID");
                raw[2] = rs.getString("Area");
                raw[3] = rs.getString("StreetName");
                raw[4] = rs.getString("StreetName2");
                for(int i = 1; i < 5; i++){
                    if(raw[i] == null || raw[i].toString().equals("null") || raw[i].toString().equals(Integer.toString(0)) ){
                        raw[i] = "";
                    }
                }
                model.addRow(raw);
            }
            jTable1.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(Served.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
